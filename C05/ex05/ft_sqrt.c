int	ft_sqrt(int nb)
{
	int	i;
	int	res;

	i = 0;
	res = 0;
	if (nb <= 1)
		return (0);
	while (res <= nb && i <= 49340)
	{
	res = i * i;
		if (res == nb)
			return (i);
		i++;
	}
	return (0);
}
