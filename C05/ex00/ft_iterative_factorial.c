int	ft_iterative_factorial(int nb)
{
	int	n;

	n = nb - 1;
	if (nb == 0)
		return (1);
	if (nb < 0)
		return (0);
	while (n > 0)
	{
		nb *= n--;
	}
	return (nb);
}
